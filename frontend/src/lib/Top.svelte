<Heading tag="h2" class="mt-5 mb-2">Cas-OFFinder</Heading>
<Heading tag="h5" class="font-light mb-5">A webtool for searching potential off-target sites based on VCF file</Heading>
<P class="mb-5">
    Cas-OFFinder is a web-based genomic search tool designed to identify potential off-target sites 
    in different organism's genome for CRISPR editing. This tool searches all possible sites that can be 
    cleaved by specific Cas protiens in the entire individual DNA. The lack of complete representation of 
    genetic variations in the refernce genome can lead to challenges in analyzing the genetic makeup of 
    diverse populations. Therefore, this tool is designed to identify potential off-target sites based 
    on not only considering a reference genome but also individual genetic variants.
</P>
